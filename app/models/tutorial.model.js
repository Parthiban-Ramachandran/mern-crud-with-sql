const sql = require("./db.js");

const User = function(user) {
  this.user_name = user.user_name;
  this.age = user.age;
  this.gender = user.gender;
};

User.postUser = (newUser, result) => {
  sql.query("INSERT INTO users SET ?", newUser, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    console.log("created user: ", { id: res.insertId, ...newUser });
    result(null, { id: res.insertId, ...newUser });
  });
};

User.getUserById = (id, result) => {
  sql.query(`SELECT * FROM users WHERE user_id = ${id}`, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    if (res.length) {
      console.log("found user: ", res[0]);
      result(null, res[0]);
      return;
    }

    result({ kind: "not_found" }, null);
  });
};

User.getAllUsers = (user_name, result) => {
  let query = "SELECT * FROM users";

  if (user_name) {
    query += ` WHERE user_name LIKE '%${user_name}%'`;
  }

  sql.query(query, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(null, err);
      return;
    }

    console.log("users: ", res);
    result(null, res);
  });
};


User.putUser = (id, user, result) => {
  sql.query(
    "UPDATE users SET user_name = ?, age = ?, gender = ? WHERE user_id = ?",
    [user.user_name, user.age, user.gender, id],
    (err, res) => {
      if (err) {
        console.log("error: ", err);
        result(null, err);
        return;
      }

      if (res.affectedRows == 0) {
        result({ kind: "not_found" }, null);
        return;
      }

      console.log("updated user: ", { id: id, ...user });
      result(null, { id: id, ...user });
    }
  );
};

User.deleteUser = (id, result) => {
  sql.query("DELETE FROM users WHERE user_id = ?", id, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(null, err);
      return;
    }

    if (res.affectedRows == 0) {
      result({ kind: "not_found" }, null);
      return;
    }

    console.log("deleted user with id: ", id);
    result(null, res);
  });
};


module.exports = User;
